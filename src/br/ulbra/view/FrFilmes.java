    public void controlarBtn(int op) {
        switch (op) {
            case 1:
                pnFilmes.setVisible(false);
                btSalvar2.setEnabled(true);
                break;
            case 2:
                pnFilmes.setVisible(true);
                btAlterar2.setEnabled(true);
                btExcluir2.setEnabled(true);
                btSalvar2.setEnabled(false);
                break;
            case 3:
                pnFilmes.setVisible(false);
                btAlterar2.setEnabled(true);
                btExcluir2.setEnabled(true);
                break;
            case 4:
                pnFilmes.setVisible(true);
                btAlterar2.setEnabled(false);
                btExcluir2.setEnabled(false);
                btSalvar2.setEnabled(true);

        }
        this.pack();
    }

    public void readJTable() throws SQLException {
        DefaultTableModel modelo = (DefaultTableModel) tbFilmes.getModel();
        modelo.setNumRows(0);
        FilmeDao fdao = new FilmeDao();
        for (Filme s : fdao.read()) {
            modelo.addRow(new Object[]{
                s.getId(),
                s.getTitulo(),
                s.getGenero(),
                s.getLancamento(),
                s.getTematica(),
                s.getSinopse(),
                s.getDuracao(),
                s.getDiretor()
            });
        }
    }

    public void readJTableForDesc(String titulo) throws SQLException {
        DefaultTableModel modelo
                = (DefaultTableModel) tbFilmes.getModel();
        modelo.setNumRows(0);
        FilmeDao mdao = new FilmeDao();
        for (Filme s : mdao.readForDesc(titulo)) {
            modelo.addRow(new Object[]{
                s.getId(),
                s.getTitulo(),
                s.getGenero(),
                s.getLancamento(),
                s.getTematica(),
                s.getSinopse(),
                s.getDuracao(),
                s.getDiretor()
            });
        }
    }
