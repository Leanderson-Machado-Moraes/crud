    private void btExcluir2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluir2ActionPerformed
        Filme f = new Filme();
        try {
            FilmeDao fd = new FilmeDao();
            if (JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir esse registro?",
                    "Excluir registro", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                f.setId(Integer.parseInt(edId.getText()));
                fd.delete(f);
                readJTable();
                controlarBtn(1);
            } else {
                JOptionPane.showMessageDialog(null, "Exclusão cancelada com sucesso!");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro: " + ex.getMessage());

        }
    }//GEN-LAST:event_btExcluir2ActionPerformed

